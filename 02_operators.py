### operadores comparativos ###

# operadores enteros
print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(4 <= 4)
print(3 == 4)
print(3 != 4)

# operaciones con cadenas de texto
print("hola" > "python")
print("hola" < "python")
print(len("aaaa") >= len("abaa")) # ordenacion alfabetica por ASCII
print("hola" <= "python") # cuenta de caracteres 
print("hola" == "hola")
print("hola" != "python")            
